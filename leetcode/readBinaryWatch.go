func readBinaryWatch(num int) []string {
	//	up := []int{{1, 2, 4 ,8},
	//				{3, 5, 9, 6, 10, 12},
	//				{7, 11, 13, 14},
	//				{15}}
	//	down := []int{{1, 2, 4, 8, 16, 32},
	//				{3, 5, 9, 17, 33, 6, 10, 18, 34, 12, 20, 36, 24, 40, 48},
	//				{7, 11, 19, 35, 13, 21, 37, 25, 41, 49, 14, 22, 38, 26, 42, 50, 28, 44, 52, 56},
	//				{15, 23, 39, 27, 43, 51, 29, 45, 53, 57, 30, 46, 54, 58, 60},
	//				{62, 61, 59, 55, 47, 31},
	//				{64}}

	str := []string{}

	if num == 0 {
		str = append(str, "0:00")
		return str
	}

	up := [][]string{{"1", "2", "4", "8"},
		{"3", "5", "9", "6", "10"},
		{"7", "11"}}
	down := [][]string{{"01", "02", "04", "08", "16", "32"},
		{"03", "05", "09", "17", "33", "06", "10", "18", "34", "12", "20", "36", "24", "40", "48"},
		{"07", "11", "19", "35", "13", "21", "37", "25", "41", "49", "14", "22", "38", "26", "42", "50", "28", "44", "52", "56"},
		{"15", "23", "39", "27", "43", "51", "29", "45", "53", "57", "30", "46", "54", "58"},
		{"59", "55", "47", "31"}}

	var a int
	if num > 3 {
		a = 3
	} else {
		a = num
	}
	for a != 0 {
		b := num - a
		for i := 0; i < len(up[a-1]); i++ {
			if b == 0 {
				str1 := up[a-1][i] + ":00"
				str = append(str, str1)
				continue
			}
			if b >= 6 {
				break
			}
			for j := 0; j < len(down[b-1]); j++ {
				str1 := up[a-1][i] + ":" + down[b-1][j]
				str = append(str, str1)
			}
		}
		a--
	}

	if a == 0 && num != 0  && num < 6 {
		for i := 0; i < len(down[num-1]); i++ {
			str1 := "0:" + down[num-1][i]
			str = append(str, str1)
		}
	}

	return str
}
